package com.controlConstructs;

public class ConditionalStatements {
	
	
public static void main(String[] args) {
	
	
	int num=11;
	if(num>10)
	{
		System.out.println("num is greater than :"+num);
	}
	else if(num<10)
	{
	System.out.println("num is lesser than :"+num);	
	}
	
	else
	{
		System.out.println("num is equal to "+num);
	}
	
}




}
