public class Poly2 {
	
	public void m1(int a, int b)
	{
		System.out.println("Sub: "+(a-b));
		
	}
	public void m1(int a, int b, int c)
	{
		System.out.println("Sum:- "+(a+b+c));
		
	}
	public void m2(int a, int b, int c,int d)
	{
		System.out.println("Mult :- "+(a*b*c*d));
		
	}

}
